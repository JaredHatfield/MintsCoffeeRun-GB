void mint_sprite_show(UINT8 first_tile, UINT8 first_sprite, UINT8 frame, UINT8 x, UINT8 y) {
    UINT8 sprite_index = first_sprite;
    UINT8 tile_index = first_tile + (frame * MINT_SPRITE_TILES_PER_FRAME);
    UINT8 row;
    UINT8 col;

    for (row = 0; row != MINT_SPRITE_HEIGHT_TILES; ++row) {
        for (col = 0; col != MINT_SPRITE_WIDTH_TILES; ++col) {
            set_sprite_tile(sprite_index, tile_index);
            move_sprite(sprite_index, x + (col << 3), y + (row << 3));
            ++sprite_index;
            ++tile_index;
        }
    }
}
